    def _load_progress(self) -> Dict:
        """진행 상황 로드"""
        if os.path.exists(self.config.progress_file):
            try:
                with open(self.config.progress_file, 'r', encoding='utf-8') as f:
                    return json.load(f)
            except Exception as e:
                logger.warning(f"진행 상황 로드 실패: {e}")
        
        return {
            "current_stage": 1,
            "completed_stages": [],
            "stage_progress": {},
            "total_collected": {
                "naver_complexes": 0,
                "molit_transactions": 0
            },
            "last_update": datetime.now().isoformat()
        }
    
    def _save_progress(self):
        """진행 상황 저장"""
        self.progress_data["last_update"] = datetime.now().isoformat()
        try:
            with open(self.config.progress_file, 'w', encoding='utf-8') as f:
                json.dump(self.progress_data, f, ensure_ascii=False, indent=2)
        except Exception as e:
            logger.error(f"진행 상황 저장 실패: {e}")
    
