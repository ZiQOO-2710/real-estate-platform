    def _generate_stage_regions(self, stage: int) -> List[Dict]:
        """단계별 지역 생성"""
        stage_data = self.stages.get(stage, {})
        regions = []
        
        trade_types = [
            {"name": "매매", "code": "A01"},
            {"name": "전세", "code": "B01"},
            {"name": "월세", "code": "B02"}
        ]
        
        for city, gus in stage_data.get("regions", {}).items():
            for gu, dongs in gus.items():
                for dong in dongs:
                    for trade_type in trade_types:
                        regions.append({
                            "city": city,
                            "gu": gu,
                            "dong": dong,
                            "trade_type": trade_type["name"],
                            "trade_code": trade_type["code"],
                            "region_key": f"{city}_{gu}_{dong}_{trade_type['name']}",
                            "stage": stage
                        })
        
        return regions
    
    def get_next_stage_regions(self, max_regions: int = 300) -> Tuple[List[Dict], int]:
        """다음 단계 지역 반환"""
        current_stage = self.progress_data.get("current_stage", 1)
        
        # 현재 단계가 완료되었는지 확인
        if current_stage in self.progress_data.get("completed_stages", []):
            current_stage += 1
            self.progress_data["current_stage"] = current_stage
        
        # 마지막 단계를 넘었는지 확인
        if current_stage > 7:
            logger.info("모든 단계 완료!")
            return [], current_stage
        
        # 현재 단계 지역 생성
        regions = self._generate_stage_regions(current_stage)
        
        # 제한된 수의 지역만 반환
        limited_regions = regions[:max_regions]
        
        stage_info = self.stages[current_stage]
        logger.info(f"단계 {current_stage}: {stage_info['name']}")
        logger.info(f"전체 {len(regions)}개 지역 중 {len(limited_regions)}개 지역 처리 예정")
        
        return limited_regions, current_stage
    
    def mark_stage_completed(self, stage: int):
        """단계 완료 표시"""
        if stage not in self.progress_data.get("completed_stages", []):
            self.progress_data["completed_stages"].append(stage)
            self.progress_data["current_stage"] = stage + 1
            self._save_progress()
            
            stage_info = self.stages[stage]
            logger.info(f"단계 {stage} 완료: {stage_info['name']}")
    
    def get_progress_summary(self) -> Dict:
        """진행 상황 요약"""
        return {
            "current_stage": self.progress_data.get("current_stage", 1),
            "completed_stages": self.progress_data.get("completed_stages", []),
            "total_stages": 7,
            "completion_rate": len(self.progress_data.get("completed_stages", [])) / 7 * 100,
            "total_collected": self.progress_data.get("total_collected", {}),
            "last_update": self.progress_data.get("last_update", ""),
            "stage_names": {i: stage["name"] for i, stage in self.stages.items()}
        }
    
